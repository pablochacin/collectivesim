package collectivesim.visualization.network;

public class AgentRenderingException extends Exception {

	public AgentRenderingException() {
	}

	public AgentRenderingException(String message) {
		super(message);
	}

	public AgentRenderingException(Throwable cause) {
		super(cause);

	}

	public AgentRenderingException(String message, Throwable cause) {
		super(message, cause);

	}

}
